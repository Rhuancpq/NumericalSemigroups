unordered_map<int, int> compute_delta(set<int> G, set<int> gen){
    int g = G.size();
    unordered_map<int, int> delta;
    for(auto x : G)
        delta[x] = 0;
    delta[0] = 1;
    for(auto x : gen)
        delta[x] = 1;

    for (int i = 1; i <= 3*g; i++){
        if(G.count(i) == 1 || gen.count(i) == 1){
            continue;
        }

        for (int j = 1; j <= i/2; j++){
            if(delta[j])
                delta[i]++;
        }

        if(delta[i] != 0){
            delta[i]++;
        }
    }
    
    return delta;
}

